package questionfour;

// 2. Write a program to print default values of all primitive types.
public class PrimitiveDatatypes {

	static int int_num;
	static char char_var;
	static float float_num;
	static boolean bool_var;
	static byte byte_var;
	static short short_num;
	static long long_num;
	static double dbl_num;
	
	public static void main(String[] args) {

		System.out.println("Default value of int is "+ int_num);
		System.out.println("Default value of char is "+ char_var);
		System.out.println("Default value of float is "+ float_num);
		System.out.println("Default value of boolean is "+ bool_var);
		System.out.println("Default value of byte is "+ byte_var);
		System.out.println("Default value of short is "+ short_num);
		System.out.println("Default value of long is "+ long_num);
		System.out.println("Default value of double is "+ dbl_num);
	}

}
